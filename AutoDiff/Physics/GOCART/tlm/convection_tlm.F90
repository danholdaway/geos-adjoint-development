!  tcnv(:,:,:) = tcnv(:,:,:) + bcnv(:,:,:)
    INTEGER, INTENT(IN) :: i1, i2, j1, j2, km, n1, n2, dt_30m
    CHARACTER(len=*) :: aero_type
