!----------------------------
      CALL F_AEROSOL(i1, i2, j1, j2, km, kc, f, bxheight, vud)
    CASE ('dust') 
!     ELSE IF ( n == n1 ) THEN
!        !----------------------------
!        ! HYDROPHOBIC (aerosol)
!        !----------------------------
!        ! Force not to be lost in convective updraft for now
!        F    = 0.0
!     END IF
!----------------------------
! DUST (aerosol) (all dust bins)
!----------------------------
      CALL F_AEROSOL(i1, i2, j1, j2, km, kc, f, bxheight, vud)
    CASE ('sea_salt') 
!----------------------------
! seasalt aerosol (accum mode and coarse mode)
!----------------------------
      CALL F_AEROSOL(i1, i2, j1, j2, km, kc, f, bxheight, vud)
    CASE ('co') 
      IF (lsadirect) THEN
        CALL F_AEROSOL(i1, i2, j1, j2, km, kc, f, bxheight, vud)
      ELSE
!----------------------------
! ALL CO ARE HYDROPHOBIC
!----------------------------
! Force not to be lost in convective updraft for now
        f = 0.0
      END IF
    END SELECT
